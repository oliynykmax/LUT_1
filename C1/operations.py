first_number = int(input("Enter the first number: "))
second_number = int(input("Enter the second number: "))

sum_result = first_number + second_number
print("Their sum:", sum_result)

difference_result = first_number - second_number
print("Their difference:", difference_result)

product_result = first_number * second_number
print("Their product:", product_result)

division_result = first_number / second_number
print("Their division:", division_result)

quotient_result = first_number // second_number
print("Their quotient:", quotient_result)

remainder_result = first_number % second_number
print("Their remainder:", remainder_result)

power_result = first_number ** second_number
print("Their power:", power_result)